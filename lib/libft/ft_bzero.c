#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptrs;
	unsigned int	i;

	ptrs = s;
	i = 0;
	while (i < n)
	{
		ptrs[i] = '\0';
		i++;
	}
}
