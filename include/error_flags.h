#ifndef ERROR_FLAGS_H
# define ERROR_FLAGS_H

/*
   mask cheatsheet:
     00000000 00000000 00000000 00000001 -> 0x1        -> F_NORTH
     00000000 00000000 00000000 00000010 -> 0x2        -> F_SOUTH
     00000000 00000000 00000000 00000100 -> 0x4        -> F_WEST
     00000000 00000000 00000000 00001000 -> 0x8        -> F_EAST
     00000000 00000000 00000000 00010000 -> 0x10       -> F_FLOOR
     00000000 00000000 00000000 00100000 -> 0x20       -> F_CEILING
     00000000 00000000 00000000 00111111 -> 0x3F       -> ALL_TEXTURES
	 00000000 00000000 00000000 10000000 -> 0x80       -> REPEATED_TEXTURE

     00100000 00000000 00000000 00000000 -> 0x20000000 -> WRONG_DATA
     00010000 00000000 00000000 00000000 -> 0x10000000 -> BAD_SITE_MAP
     00001000 00000000 00000000 00000000 -> 0x4000000  -> NO_MAP 

     00000000 00000000 00000001 00000000 -> 0x100      -> FORBIDDEN_MAP
     00000000 00000000 00000010 00000000 -> 0x200      -> MAP_NOT_CLOSED
     00000000 00000000 00000100 00000000 -> 0x400      -> REPEADED_PLAYER
     00000000 00000000 00001000 00000000 -> 0x800      -> NO_PLAYER
     00000000 00000000 00010000 00000000 -> 0x1000     -> MAP_BREAK

	 00000000 00000001 00000000 00000000 -> 0x10000    -> SIZE_NORTH
	 00000000 00000010 00000000 00000000 -> 0x20000    -> SIZE_SOUTH
	 00000000 00000100 00000000 00000000 -> 0x40000    -> SIZE_WEST
	 00000000 00001000 00000000 00000000 -> 0x80000    -> SIZE_EAST

	 01000000 00000000 00000000 00000000 -> 0x40000000 -> FAILURE
*/
typedef enum e_error_flags
{
	F_NORTH = 0x1,
	F_SOUTH = 0x2,
	F_WEST = 0x4,
	F_EAST = 0x8,
	F_FLOOR = 0x10,
	F_CEILING = 0x20,
	ALL_TEXTURES = 0x3F,
	FAILURE = 0x40000000,
	REPEATED_TEXTURE = 0X80,
	WRONG_DATA = 0x20000000,
	NO_MAP = 0x4000000,
	BAD_SITE_MAP = 0x10000000,
	FORBIDDEN_MAP = 0x100,
	MAP_NOT_CLOSED = 0x200,
	REPEATED_PLAYER = 0x400,
	NO_PLAYER = 0x800,
	MAP_BREAK = 0x1000,
	SIZE_NORTH = 0x10000,
	SIZE_SOUTH = 0x20000,
	SIZE_WEST = 0x40000,
	SIZE_EAST = 0x80000,
}	t_error_flags;

#endif
